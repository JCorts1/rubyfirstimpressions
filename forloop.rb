
#Using do and element inside of bars
friends = ["Kevin", "Karen", "Oscar", "Angela", "Andy"]

friends.each do |friend|
  puts friend
end


#Classic method.
friends = ["Kevin", "Karen", "Oscar", "Angela", "Andy"]

for element in friends
  puts element
end


#Index of array in For Loop
friends = ["Kevin", "Karen", "Oscar", "Angela", "Andy"]

for index in 0..5
  puts index
end



